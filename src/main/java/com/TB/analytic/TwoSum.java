package com.TB.analytic;

public class TwoSum {
private int firdstaddress;//用来记录最后一个位置
private int secondaddress;
private int count;//用来标记

public int getFirdstaddress() {
	return firdstaddress;
}
public void setFirdstaddress(int firdstaddress) {
	this.firdstaddress = firdstaddress;
}
public int getSecondaddress() {
	return secondaddress;
}
public void setSecondaddress(int secondaddress) {
	this.secondaddress = secondaddress;
}
public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}

}
