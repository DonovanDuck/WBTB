package com.TB.analytic.library;

public class Library {
private int vid;
private int noteId;
private String userNumber;
private double value;
private String specialWord;
private String keysentence;
private String time;

public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}
public int getVid() {
	return vid;
}
public void setVid(int vid) {
	this.vid = vid;
}
public int getNoteId() {
	return noteId;
}
public void setNoteId(int noteId) {
	this.noteId = noteId;
}
public String getUserNumber() {
	return userNumber;
}
public void setUserNumber(String userNumber) {
	this.userNumber = userNumber;
}
public double getValue() {
	return value;
}
public void setValue(double value) {
	this.value = value;
}
public String getSpecialWord() {
	return specialWord;
}
public void setSpecialWord(String specialWord) {
	this.specialWord = specialWord;
}
public String getKeysentence() {
	return keysentence;
}
public void setKeysentence(String keysentence) {
	this.keysentence = keysentence;
}


}
